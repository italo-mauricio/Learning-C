#include<stdio.h>

int main()

{

    int num = 112;
    printf("%i\n", num );

    float f = 2.2;
    printf("%f", f);


    // integer =  %i ou %d
    // float ou double = %f
    // char = %c
    // string = %s
    // %u = número inteiro sem o sinal (módulo)
    // %p = endereço de memória
    // %e ou %E = imprimir numeros com notação científica
    // %% = imprimi somente 1 símbolo de porcentagem

    int x = 10;
    printf("%i\n", x);

    float ff = 4.12;
    printf("%f\n", ff);

    printf("%i - %f \n", x, ff);


    return 0;


}