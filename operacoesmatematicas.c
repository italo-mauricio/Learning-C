#include<stdio.h>
#include<stdlib.h>


/*

        usando operadores matemáticos em C

        soma = +
        subtração = -
        multiplicação = *
        divisão = /

        Conversão de tipos


*/

int main(){


    int a = 10, b = 20;

    printf("\nsoma: %d\n", a + b);
    printf("\nSubtracao: %d\n", a - b);
    printf("\nMultiplicacao: %d\n", a * b);
    printf("\nDivisao: %f\n", (float)a / b);


    return 0;

}


